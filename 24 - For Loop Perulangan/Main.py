# Perulangan (loop)

# for kondisi:
#    action

angka2_list= [0,1,3,5,7] # this is list
print(angka2_list)

for i in angka2_list:
    print(f"i now -> {i}")

print("end from program\n")

# this with range
angka2_range = range(5)

for i in angka2_range:
        print(f"i now -> {i}")

print("akhir dari program 2\n")

angka2_range = range(1,10)

for i in angka2_range:
        print(f"i now -> {i}")

print("akhir dari program 3\n")

